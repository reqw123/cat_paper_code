    cmap = plt.colormaps['YlOrRd_r']  # 反轉色圖：紅->橙->黃
    
    # 繪製每個異常幀並標註
    for rank, (idx, row) in enumerate(ab_frames.iterrows(), 1):
        frame_num = row["frame"]
        score = row["score"]
        color = cmap(norm(score))
        
        # 繪製大點
